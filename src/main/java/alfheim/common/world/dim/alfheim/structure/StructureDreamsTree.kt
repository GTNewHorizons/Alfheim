	fun canBePlaced(world: World, x: Int, y: Int, z: Int, block: Block): Boolean {
		if (world.getTileEntity(x, y, z) != null) return false
		
		val at = world.getBlock(x, y, z)
		if (at.getBlockHardness(world, x, y, z) == -1f) return false
		
		if (block === leaves) {
			return ASJUtilities.isBlockReplaceable(at)
		}
		if (block === log) {
			return (ASJUtilities.isBlockReplaceable(at) || at.material in forLog)
		}
		return false
	}
	
	companion object {
		
		val forLog = arrayOf(Material.wood, Material.ground, Material.grass)
		
		val validSpawnMaterial: Array<Material>
			get() = arrayOf(Material.grass, Material.ground)
		
		fun LocationIsValidSpawn(world: World, x: Int, y: Int, z: Int): Boolean {
			var y = y
			var distanceToAir = 0
			var checkBlock = world.getBlock(x, y, z)
			
			while (checkBlock !== Blocks.air) {
				distanceToAir++
				checkBlock = world.getBlock(x, y + distanceToAir, z)
			}
			
			if (distanceToAir > 1) {
				return false
			}
			
			y += distanceToAir - 1
			
			val block = world.getBlock(x, y, z)
			val blockAbove = world.getBlock(x, y + 1, z)
			val blockBelow = world.getBlock(x, y - 1, z)
			
			for (i in validSpawnMaterial) {
				if (blockAbove !== Blocks.air)
					return false
				if (block.material === i)
					return true
				else if (block.material === Material.snow && blockBelow.material === i)
					return true
				else if (block.material === Material.plants && blockBelow.material === i)
					return true
			}
			return false
		}
	}
